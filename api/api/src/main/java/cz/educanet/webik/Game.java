package cz.educanet.webik;

public class Game {

    public int id;
    public String name;
    public int price;
    public String developer;
    public String publisher;
    public int  rating;
    public int ageRestriction;


    public int getIdco() {
        return idco;
    }
    public void setIdco(int idco) {
        this.idco = idco;
    }
    public String getJmeno() {
        return name;
    }
    public void setJmeno(String name) {
        this.name = name;
    }
    public int getCena() {
        return price;
    }
    public void setCena(int price) {
        this.price = price;
    }
    public String getVyvojar() {
        return developer;
    }
    public void setVyvojar(String developer) {
        this.developer = developer;
    }
    public String getVydavatel() {
        return publisher;
    }
    public void setVydavatel(String publisher) {
        this.publisher = publisher;
    }
    public int getRating() {
        return rating;
    }
    public void setRating(int rating) {
        this.rating = rating;
    }
    public int getAgeRestriction() {
        return ageRestriction;
    }
    public void setAgeRestriction(int ageRestriction) {
        this.ageRestriction = ageRestriction;
    }




}
